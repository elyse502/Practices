# Finding average of elements in a Series
import pandas as pd
s1 = pd.Series([10, 120, 30, 410, 50])
print(s1)
print()
print(">> Average of series is : ", s1.mean())
print()

# Finding maximum value in the series
print(">> Maximum value of series is : ", s1.max())
print()

# Finding minimum value in the series
print(">> Minimum value of series is : ", s1.min())
print()
